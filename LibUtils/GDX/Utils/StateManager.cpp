#include "StateManager.h"

namespace GDX
{
    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    StateManager::StateManager()
        :
        StateManager(GameStates::Inactive)
    {
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    StateManager::StateManager(GameStates state)
        :
        m_currentState(state)
    {
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    GameStates StateManager::Get()
    {
        return m_currentState;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void StateManager::Set(GameStates state)
    {
        m_currentState = state;
    }
}


