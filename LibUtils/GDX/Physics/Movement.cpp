#include "Movement.h"

namespace GDX
{
    const char *aliases[2][3]
    {
        {"Left", "Still", "Right"},
        {"Up  ", "Still", "Down "},
    };

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    const char *Movement::GetAliasX(int value) const
    {
        return aliases[0][value + 1];
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    const char *Movement::GetAliasY(int value) const
    {
        return aliases[1][value + 1];
    }
}


