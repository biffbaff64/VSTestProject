#include "Direction.h"

namespace GDX
{
    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------
    
    Direction::Direction()
        :
        m_x(DIRECTION_STILL),
        m_y(DIRECTION_STILL)
    {
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    Direction::Direction(int x, int y)
        :
        m_x(x),
        m_y(y)
    {
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    Direction::Direction(const Direction &direction)
        :
        m_x(direction.m_x),
        m_y(direction.m_y)
    {
    };

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void Direction::Set(const Direction &direction)
    {
        m_x = direction.m_x;
        m_y = direction.m_y;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void Direction::Set(int x, int y)
    {
        m_x = x;
        m_y = y;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void Direction::SetX(int x)
    {
        m_x = x;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void Direction::SetY(int y)
    {
        m_y = y;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void Direction::StandStill()
    {
        m_x = DIRECTION_STILL;
        m_y = DIRECTION_STILL;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    bool Direction::HasDirection() const
    {
        return (m_x != DIRECTION_STILL) || (m_y != DIRECTION_STILL);
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    int Direction::GetX() const
    {
        return m_x;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    int Direction::GetY() const
    {
        return m_y;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    int Direction::GetFlippedX() const
    {
        return m_x * -1;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    int Direction::GetFlippedY() const
    {
        return m_y * -1;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void Direction::Toggle()
    {
        if (m_x != DIRECTION_STILL)
        {
            ToggleX();
        }

        if (m_y != DIRECTION_STILL)
        {
            ToggleY();
        }
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void Direction::ToggleX()
    {
        m_x *= -1;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void Direction::ToggleY()
    {
        m_y *= -1;
    }
}