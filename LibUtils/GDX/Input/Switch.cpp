#include "Switch.h"

namespace GDX
{
    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    Switch::Switch()
    {
        m_IsPressed = false;
        m_IsDisabled = false;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void Switch::Press()
    {
        if (!m_IsDisabled)
        {
            m_IsPressed = true;
        }
    }

    //------------------------------------------------------------------------------------------------------
    // Press switch IF the supplied condition is TRUE,
    // else leave the switch state unchanged.
    //------------------------------------------------------------------------------------------------------

    void Switch::PressConditional(bool condition)
    {
        if (!m_IsDisabled && condition)
        {
            m_IsPressed = true;
        }
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void Switch::Release()
    {
        m_IsPressed = false;
    }

    //------------------------------------------------------------------------------------------------------
    // Release switch IF the supplied condition is TRUE,
    // else leave the switch state unchanged.
    //------------------------------------------------------------------------------------------------------

    void Switch::ReleaseConditional(bool condition)
    {
        if (!m_IsDisabled && condition)
        {
            m_IsPressed = false;
        }
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    bool Switch::IsPressed() const
    {
        return m_IsPressed;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    bool Switch::IsDisabled() const
    {
        return m_IsDisabled;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void Switch::TogglePressed()
    {
        m_IsPressed = !m_IsPressed;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void Switch::ToggleDisabled()
    {
        m_IsDisabled = !m_IsDisabled;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void Switch::SetDisabled(bool state)
    {
        m_IsDisabled = state;
    }
}


