#include "Shape.h"

namespace GDX
{
	//------------------------------------------------------------------------------------------------------
	//------------------------------------------------------------------------------------------------------

	Shape::Shape()
		:
		m_x(0),
		m_y(0),
		m_width(0),
		m_height(0),
		m_size()
	{
	}

	//------------------------------------------------------------------------------------------------------
	//------------------------------------------------------------------------------------------------------

	Shape::~Shape()
	{
	}

	//------------------------------------------------------------------------------------------------------
	//------------------------------------------------------------------------------------------------------

	void Shape::SetSize(Vec2<int> size)
	{
		m_size = size;
	}

	//------------------------------------------------------------------------------------------------------
	//------------------------------------------------------------------------------------------------------

	void Shape::SetSize(Vec2<float> size)
	{
		m_size.m_x = (int) size.m_x;
		m_size.m_y = (int) size.m_y;
	}

	//------------------------------------------------------------------------------------------------------
	//------------------------------------------------------------------------------------------------------

	int Shape::GetX() const
	{
		return m_x;
	}

	//------------------------------------------------------------------------------------------------------
	//------------------------------------------------------------------------------------------------------

	int Shape::GetY() const
	{
		return m_y;
	}

	//------------------------------------------------------------------------------------------------------
	//------------------------------------------------------------------------------------------------------

	int Shape::GetWidth() const
	{
		return m_width;
	}

	//------------------------------------------------------------------------------------------------------
	//------------------------------------------------------------------------------------------------------

	int Shape::GetHeight() const
	{
		return m_height;
	}

	//------------------------------------------------------------------------------------------------------
	//------------------------------------------------------------------------------------------------------

	Vec2<int> Shape::GetSize() const
	{
		return m_size;
	}
}

