#include "SimpleVec3F.h"

namespace GDX
{
    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    SimpleVec3F::SimpleVec3F() : SimpleVec3F(0, 0, 0)
    {
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    SimpleVec3F::SimpleVec3F(float x, float y, float z)
    {
        m_x = x;
        m_y = y;
        m_z = z;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    SimpleVec3F::SimpleVec3F(SimpleVec3F const& vec3) = default;

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void SimpleVec3F::Add(float x, float y, float z)
    {
        m_x += x;
        m_y += y;
        m_z += z;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void SimpleVec3F::AddX(float x)
    {
        m_x += x;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void SimpleVec3F::AddY(float y)
    {
        m_y += y;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void SimpleVec3F::AddZ(float z)
    {
        m_z += z;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void SimpleVec3F::Sub(float x, float y, float z)
    {
        m_x -= x;
        m_y -= y;
        m_z -= z;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void SimpleVec3F::SubX(float x)
    {
        m_x -= x;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void SimpleVec3F::SubY(float y)
    {
        m_y -= y;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void SimpleVec3F::SubZ(float z)
    {
        m_z -= z;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void SimpleVec3F::Set(float x, float y, float z)
    {
        m_x = x;
        m_y = y;
        m_z = z;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void SimpleVec3F::Set(const SimpleVec3F &vec3)
    {
        m_x = vec3.m_x;
        m_y = vec3.m_y;
        m_z = vec3.m_z;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void SimpleVec3F::Mul(float x, float y, float z)
    {
        m_x *= x;
        m_y *= y;
        m_z *= z;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void SimpleVec3F::Mul(const SimpleVec3F &vec3)
    {
        m_x *= vec3.m_x;
        m_y *= vec3.m_y;
        m_z *= vec3.m_z;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    bool SimpleVec3F::IsEmpty() const
    {
        return ((m_x == 0) && (m_y == 0) && (m_z == 0));
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void SimpleVec3F::SetEmpty()
    {
        m_x = 0;
        m_y = 0;
        m_z = 0;
    }
}


