#include "SimpleVec3.h"

namespace GDX
{
    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    SimpleVec3::SimpleVec3()
        :
        SimpleVec3(0, 0, 0)
    {
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    SimpleVec3::SimpleVec3(int x, int y, int z)
    {
        m_x = x;
        m_y = y;
        m_z = z;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    SimpleVec3::SimpleVec3(SimpleVec3 const& vec3)
    {
        m_x = vec3.m_x;
        m_y = vec3.m_y;
        m_z = vec3.m_z;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void SimpleVec3::Add(int x, int y, int z)
    {
        m_x += x;
        m_y += y;
        m_z += z;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void SimpleVec3::Sub(int x, int y, int z)
    {
        m_x -= x;
        m_y -= y;
        m_z -= z;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void SimpleVec3::Set(int x, int y, int z)
    {
        m_x = x;
        m_y = y;
        m_z = z;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void SimpleVec3::Set(const SimpleVec3 &vec3)
    {
        m_x = vec3.m_x;
        m_y = vec3.m_y;
        m_z = vec3.m_z;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void SimpleVec3::Mul(int x, int y, int z)
    {
        m_x *= x;
        m_y *= y;
        m_z *= z;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void SimpleVec3::Mul(const SimpleVec3 &vec3)
    {
        m_x *= vec3.m_x;
        m_y *= vec3.m_y;
        m_z *= vec3.m_z;
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    bool SimpleVec3::IsEmpty() const
    {
        return ((m_x == 0) && (m_y == 0) && (m_z == 0));
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void SimpleVec3::SetEmpty()
    {
        m_x = 0;
        m_y = 0;
        m_z = 0;
    }
}


