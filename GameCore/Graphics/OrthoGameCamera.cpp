    OrthoGameCamera::OrthoGameCamera()
        :
        OrthoGameCamera(nullptr, 0, 0)
    {
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    OrthoGameCamera::OrthoGameCamera(const char *name, float width, float height)
    {
        m_name           = name;
        m_isInUse        = false;
        m_LerpingEnabled = false;
        m_lerpVector     = GDX::Vec3Int();
        m_defaultZoom    = DEFAULT_ZOOM;

        m_view = sf::View();
        m_view.setSize(width, height);
        m_view.setCenter(width / 2, height / 2);
        m_view.setViewport(sf::FloatRect(0, 0, 1, 1));
    }

    //------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------

    void OrthoGameCamera::SetViewCentre(float x, float y)
    {
        m_view.setCenter(x, y);
    }

